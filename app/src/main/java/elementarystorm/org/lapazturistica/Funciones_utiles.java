    public static Bitmap decodeSampledBitMapFromResouce(Resources res,int resId,int reqWidth,int redHeidht){
        final BitmapFactory.Options options=new BitmapFactory.Options();
        options.inJustDecodeBounds=true;
        BitmapFactory.decodeResource(res,resId,options);
        options.inSampleSize=calculateInSampleSize(options,reqWidth,redHeidht);
        options.inJustDecodeBounds=false;
        return BitmapFactory.decodeResource(res, resId, options);
    }

    private static int calculateInSampleSize(BitmapFactory.Options options, int reqWidth, int redHeidht) {
        final int height=options.outHeight;
        final int widht=options.outWidth;
        int inSampleSize=1;
        if(height>redHeidht||widht>reqWidth){
            final int halfHeight=height/2;
            final int halfWidht=widht/2;
            while((halfHeight/inSampleSize)>redHeidht&&(halfWidht/inSampleSize)>reqWidth){
                inSampleSize*=2;
            }
        }
        return inSampleSize;
    }

